void i18n
  .use(LanguageDetector)
  .use(initReactI18next)
  .init({
    resources,
    fallbackLng: "en",
    supportedLngs: [...supportedLanguages],
    interpolation: { escapeValue: false },
    detection: {
      order: ["navigator", "htmlTag"],
      caches: [],
    },
  })
  .then(() => {
    document.documentElement.lang = i18n.resolvedLanguage?.startsWith("fr") ? "fr" : "en";
  });

